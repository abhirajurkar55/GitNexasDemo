#Deletion operation

py_List=[10,44554,15256,25244,15243669,85200145,20005,2155,4553454,66535,223,223,234,55,65,69,66,66]
print(py_List)

#remove(x):to remove a single element fromm the list

py_List.remove(66)
print(py_List)

py_List.remove(85200145)
print(py_List)

#pop(x):it will delete value using index number , it no value is given it will delete the last number

py_List.pop(0)
print(py_List)

py_List.pop()
print(py_List)  

#clear():it is use to clear all the elements in the list
py_List.clear()
print(py_List)

        
        