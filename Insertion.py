
#List:- List is a Collection of similar or different types of datatypes 



py_List=[10,12,13,14,50,60,75,101,801,1001,5002,401,805,609]
print(py_List)

# insert(x,x) : to insert element at the given index 
py_List.insert(4,143)
print(py_List)

py_List.insert(5,103)
print(py_List)
 
#append (x):to insert new element at the end

py_List.append(64)
print(py_List)

py_List.append(103)
print(py_List)

#extend(x):to insert multiple element at the end

py_List.extend([88,89,90])
